import Foundation
extension Int
{
    func unitConcat()->String
    {
        return "\(self) Units"
    }
    
    func gbConcat() ->String
    {
        return " \(self) GB"
    }
    
    func minutesConcat() -> String
    {
           return " \(self) Minutes"
    }
       
    
}
